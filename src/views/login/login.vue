<template>
  <div class="login-container">
    <el-form ref="loginForm" :model="loginForm" :rules="loginRules" class="login-form" auto-complete="on" label-position="left">

      <div class="title-container">
        <h3 class="title">Login Form</h3>
      </div>

      <el-form-item prop="username">
        <span class="svg-container">
          <svg-icon icon-class="user" />
        </span>
        <el-input
          ref="username"
          v-model="loginForm.username"
          placeholder="Username"
          name="username"
          type="text"
          tabindex="1"
          auto-complete="on"
        />
      </el-form-item>

      <el-form-item prop="password">
        <span class="svg-container">
          <svg-icon icon-class="password" />
        </span>
        <el-input
          :key="passwordType"
          ref="password"
          v-model="loginForm.password"
          :type="passwordType"
          placeholder="Password"
          name="password"
          tabindex="2"
          auto-complete="on"
          @keyup.enter.native="handleLogin"
        />
        <span class="show-pwd" @click="showPwd">
          <svg-icon :icon-class="passwordType === 'password' ? 'eye' : 'eye-open'" />
        </span>
      </el-form-item>

      <el-button :loading="loading" type="primary" style="width:100%;margin-bottom:30px;" @click.native.prevent="handleLogin">Login</el-button>

    </el-form>
  </div>
</template>

<script>
// import { validUsername } from '@/utils/validate'
import { login } from './api'
import utils from '@/utils/utils'
// import  routerIndex  from '@/router/index';
// import  routerJs from '@/router/router';
// import Layout from '@/layout'
export default {
  name: 'Login',
  data() {
    return {
      loginForm: {
        username: 'dj_user',
        password: 'dj_user@123'
      },
      loginRules: {
        username: [{ required: true, trigger: 'blur' }],
        password: [{ required: true, trigger: 'blur' }]
      },
      loading: false,
      passwordType: 'password',
      redirect: undefined
    }
  },
  watch: {
    $route: {
      handler: function(route) {
        // this.redirect = route.query && route.query.redirect
      },
      immediate: true
    }
  },
  methods: {
    showPwd() {
      if (this.passwordType === 'password') {
        this.passwordType = ''
      } else {
        this.passwordType = 'password'
      }
      this.$nextTick(() => {
        this.$refs.password.focus()
      })
    },
    userLogin(){
      login(this.loginForm).then(res => {
        const data = res.data;
        if(data.code == 200){
          let getData = data.data;
          sessionStorage.setItem('isLogin',true);
          sessionStorage.setItem('djwjsc_token',getData.token);
          sessionStorage.setItem('dj_userId',getData.userId);
          sessionStorage.setItem('dj_userName',getData.userName);

          utils.initRouter(this);
          // const getRouterJs = routerJs.filter(item=>item.meta.id==data.data.userId);

          // const routerArr = getRouterJs.map(item=>{
          //   return {
          //     path:'/',
          //     component:Layout,
          //     redirect:item.path,
          //     children:[item]
          //   }
          // })

          // const obj404 = {
          //   path:'/*',
          //   component: () => import('@/views/404'),
          //   hidden:true
          // }
          // routerArr.push(obj404)
          // sessionStorage.setItem('dj_meanList',JSON.stringify(routerArr))

          // this.$router.options.routes = routerArr;
          // routerIndex.addRoutes(routerArr)
          this.$router.push({ path: this.redirect || '/' });
        }
      })
    },
    handleLogin() {
      this.$refs.loginForm.validate(valid => {
        if (valid) {
          this.loading = true;
          this.userLogin()
          // this.$router.push({ path: this.redirect || '/' })
        } else {
          console.log('error submit!!')
          return false
        }
      })
    }
  }
}
</script>

